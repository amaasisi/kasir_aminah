    private void tb_detailMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_detailMouseClicked
    // TODO add your handling code here:
    int i = tb_detail.getSelectedRow();
    if (i== -1){
        return;
    }
    String id_det = (String) tm.getValueAt(i, 0);
    iddetail.setText(id_det);
    String idpen = (String) tm.getValueAt(i, 1);
    cbid_penjualan.setSelectedItem (idpen);
    String idpro = (String) tm.getValueAt(i, 2);
    cbidpro.setSelectedItem (idpro);
    String jml = (String) tm.getValueAt(i, 3);
    jumlah.setText(jml);
    String sub = (String) tm.getValueAt(i, 4);
    total.setText(sub);    
    }//GEN-LAST:event_tb_detailMouseClicked

