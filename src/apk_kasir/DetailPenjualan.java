    private void refreshTable(){
        tm = new DefaultTableModel(
            null,
            new Object[] {"ID DETAIL", "ID PENJUALAN", "ID PRODUK", "JUMLAH PRODUK", "SUBTOTAL"});
        tb_detail.setModel(tm);
        tm.getDataVector().removeAllElements();
        
        try{
            PreparedStatement s = con.prepareStatement("SELECT * FROM detail_penjualan");
            ResultSet r = s.executeQuery();
            while(r.next()){
                Object[] data = {
                    r.getString(1),
                    r.getString(2),
                    r.getString(3),
                    r.getString(4),
                    r.getString(5)
                };
                tm.addRow(data);
            }
        }catch(Exception e){
            System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
        }
    }
    

